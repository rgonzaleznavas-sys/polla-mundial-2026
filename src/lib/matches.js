export function isOpen(match) {
  return new Date() < new Date(match.kickoff)
}

export function formatKickoff(kickoff) {
  return new Date(kickoff).toLocaleString('es-PA', {
    month: 'short', day: 'numeric',
    hour: '2-digit', minute: '2-digit',
    timeZone: 'America/Panama'
  })
}

export function calcPoints(pick, result) {
  if (!result || result.home_score === null || result.away_score === null) return null
  if (pick.home_score === null || pick.away_score === null) return 0

  const rH = result.home_score, rA = result.away_score
  const pH = pick.home_score, pA = pick.away_score

  if (rH === pH && rA === pA) {
    return rH === rA ? 5 : 3 // empate exacto = 5, exacto normal = 3
  }
  const rW = rH > rA ? 'H' : rH < rA ? 'A' : 'D'
  const pW = pH > pA ? 'H' : pH < pA ? 'A' : 'D'
  return rW === pW ? 1 : 0
}
